brightness = [0x05, 0x01, 0x16, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]

def checkSum(message):    
    chk = 0x00
    
    for m in message:
        chk ^= m
    
    return chk

